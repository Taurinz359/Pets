<?php

namespace Tests;

use Illuminate\Database\Capsule\Manager;
use Phinx\Config\Config;
use PHPUnit\Framework\TestCase as PHPUnit;
use Psr\Http\Message\ServerRequestInterface as Request;
use Slim\App;
use Slim\Factory\AppFactory;
use Slim\Psr7\Factory\StreamFactory;
use Slim\Psr7\Headers;
use Slim\Psr7\Request as SlimRequest;
use Slim\Psr7\Uri;
use Symfony\Component\Console\Input\StringInput;
use Symfony\Component\Console\Output\NullOutput;



class TestCase extends phpUnit
{
    protected App $app;
    protected function setUp(): void
    {
        $testingTablePath = __DIR__ . '/testing.sqlite3';
        if (file_exists($testingTablePath)) {
            unlink($testingTablePath);
        }
        parent::setUp();
        $this->app = $this->getAppInstance();
    }

    protected function refreshDatabase(): void
    {
        $configArray = require __DIR__ . '/../phinx.php';
        $config = new Config($configArray);
        $manager = new \Phinx\Migration\Manager($config, new StringInput(' '), new NullOutput());
        $manager->migrate('testing');
        $manager->seed('testing');
    }

    protected function getAppInstance(): App
    {
        $container = require __DIR__ . '/../config/bootstrap.php';

        AppFactory::setContainer($container);
        $app = AppFactory::create();

        $routes = require __DIR__ . '/../config/routes.php';
        $routes($app);

        $middleware = require __DIR__ . '/../config/middleware.php';
        $middleware($app);

        $capsule = new \Illuminate\Database\Capsule\Manager();
        $capsule->addConnection($container->get('db'));
        $capsule->setAsGlobal();
        $capsule->bootEloquent();

        return $app;
    }

    protected function createRequest(
        string $method,
        string $path,
        array  $headers = ['HTTP_ACCEPT' => 'application/json'],
        array  $cookies = [],
        array  $serverParams = []
    ): Request
    {
        $uri = new Uri ('', '', 80, $path);
        $handle = fopen('php://temp', 'w+');
        $stream = (new StreamFactory())->createStreamFromResource($handle);

        $h = new Headers();
        foreach ($headers as $name => $value) {
            $h->addHeader($name, $value);
        }
        return new SlimRequest($method, $uri, $h, $cookies, $serverParams, $stream);
    }

    protected function tearDown(): void
    {
        parent::tearDown(); // TODO: Change the autogenerated stub
        $testingTablePath = __DIR__ . '/testing.sqlite3';
        if (file_exists($testingTablePath)) {
            unlink($testingTablePath);
        }
    }
}